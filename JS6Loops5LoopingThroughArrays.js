/* Looping throught Arrays */
// Bucle a traves de arreglos

const animals = ['Grizzly Bear', 'Sloth', 'Sea Lion'];
for (let i = 0; i < animals.length; i++){
  console.log(animals[i]);
}

// Output:
// Grizzly Bear
// Sloth
// Sea Lion

