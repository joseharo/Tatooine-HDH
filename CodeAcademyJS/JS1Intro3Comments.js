/* 1. Un comentario de una sola linea se puede hacer con dos barras que preceden a este //  */

// Prints 5 to the console
console.log(5);

// Tambien puede hacerse un comentario de una linea en la misma linea de codigo al final 
console.log(5); // Prints 5 to the console

/* 2. Los comentarios de varias lineas pueden hacerse usando una barra y un asterisco / * para iniciarlo y un asterisco y una barra para finalizarlo * / (sin espacios) */

/* You can also use this syntax to comment something out in the middle of a line code: */

