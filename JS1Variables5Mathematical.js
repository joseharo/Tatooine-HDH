/* Mathematical Assignment Operators 
Operadores matematicos de asignacion  */

let w = 4; // Valor de 4 en w
w = w + 1; // A w se le suma 1
console.log(w); // Output: 5

// Otra forma de asignar un incremento a una variable

let z = 6; // Valor de 6 en z
z += 1; // z se incrementa en 
console.log(z); // Output: 7

// Otras operaciones de asignacion por medio de operadores matematicos

let x = 20;
x -= 5; // Can be written as x = x - 5
console.log(x); // Output: 15

let y = 50;
y *= 2; // Can be written as y = y * 2
console.log(y); // Output: 100

let z = 8;
z/=2; // Can be written as z = z / 2
console.log(z); // Output: 4

// Use the += mathematical assignment operator to increase the value stored in levelUp by 5.
let levelUp = 10;
levelUp += 5; // Can be written as levelUp = levelUp + 5;
console.log('The value of levelUp:', levelUp); // Output: 15

// Use the -= mathematical assignment operator to decrease the value stored in powerLevel by 100.
let powerLevel = 9001;
powerLevel -= 100; // Can be written as  powerLevel = powerLevel - 100;
console.log('The value of powerLevel:', powerLevel); // Output: 8901

// Use the *= mathematical assignment operator to multiply the value stored in multiplyMe by 11.
let multiplyMe = 32;
multiplyMe *= 11; // Can be written as multiplyMe = MultiplyMe * 11;
console.log('The value of multiplyMe:', multiplyMe); 

// Use the /= mathematical assignment operator to divide the value stored in quarterMe by 4.
let quarterMe = 1152;
quarterMe /= 4; // Can be written as quarterMe = quarterMe / 4;
console.log('The value of quarterMe:', quarterMe); // Output: 288